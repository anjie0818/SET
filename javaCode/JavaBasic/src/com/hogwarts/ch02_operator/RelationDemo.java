package com.hogwarts.ch02_operator;

/**
 * 关系运算符
 */
public class RelationDemo {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        // ==
        System.out.println(a == b);  // false
        // !=
        System.out.println(a != b);  // true
        // >
        System.out.println(a > b);  // true
        // <
        System.out.println(a < b);  // false
        // >=
        System.out.println(a >= b);  // true
        // <=
        System.out.println(a <= b);  // false
    }
}
