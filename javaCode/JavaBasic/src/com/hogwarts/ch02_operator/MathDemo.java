package com.hogwarts.ch02_operator;

/**
 * 算术运算符
 */
public class MathDemo {
    public static void main(String[] args) {
        // 初始化变量
        int a = 20;
        int b = 10;
        // 算术运算：+，-，*，/，%
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(20 / 3);
        System.out.println(20 % 3);
    }
}
