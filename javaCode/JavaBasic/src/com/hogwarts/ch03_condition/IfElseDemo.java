package com.hogwarts.ch03_condition;

/**
 * if-else 语句
 */
public class IfElseDemo {
    public static void main(String[] args) {
        // 分数
        int hogwarts = 79;
        // if...else 逻辑
        if (hogwarts >= 80){
            System.out.println("优秀");
        }else{
            System.out.println("加油");
        }
    }
}
