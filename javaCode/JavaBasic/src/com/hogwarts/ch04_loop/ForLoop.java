package com.hogwarts.ch04_loop;

/**
 * for 循环语句
 * 打印数字1-9
 */
public class ForLoop {
    public static void main(String[] args) {
        // for循环
        for(int i=1; i<10; i++){
            // 语句
            System.out.println(i);
        }
        System.out.println("执行完毕！");
    }
}
