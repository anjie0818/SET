package com.hogwarts.ch05_array;

/**
 * 数组访问
 */
public class ArrayAccess {
    public static void main(String[] args) {
        // 声明并初始化数组
        int[] arrayA = {1, 2, 3, 4, 5};
        // 获取第一个元素
        int first = arrayA[2];
        System.out.println(first);

        // 打印数字5
        System.out.println(arrayA[4]);

    }
}
