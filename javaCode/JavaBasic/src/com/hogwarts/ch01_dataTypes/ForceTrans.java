package com.hogwarts.ch01_dataTypes;

/**
 * 强制类型转换
 */
public class ForceTrans {
    public static void main(String[] args) {
        // 初始化变量
        double d = 3.1415;
        // 大转小，使用强制转换
        int i = (int)d;
        System.out.println(i);
    }
}
